local a, b, c, d
function love.load()
	assert(not string.normalizePath, "string function")
	assert(not ("").normalizePath, "string method")
	b = love.graphics.newImage("logo.png")
	c = love.graphics.newFont()
	d = love.graphics.newImageFont("font.png", "STARFLE™!|x") -- UTF-8
end
function love.update(dt)
	a = dt
end
function love.draw()
	love.graphics.draw(b, 0, 0)
	love.graphics.setColor(0, 0.5, 0, 1)
	love.graphics.rectangle("fill", 120, 140, 100, 100)
	love.graphics.setColor(1, 0.5, 0.5, 1)
	love.graphics.rectangle("fill", 19, 19, 2, 2)
	love.graphics.rectangle("fill", 199, 19, 2, 2)
	love.graphics.rectangle("fill",  10, 100, 20, 20)
	love.graphics.rectangle("line",  40, 100, 20, 20)
	love.graphics.rectangle("fill",  70, 100, 20, 20, 5, 5)
	love.graphics.rectangle("line", 100, 100, 20, 20, 5, 5)
	love.graphics.rectangle("fill", 10, 140, 100, 100, 10, 10)
	love.graphics.rectangle("line", 120, 140, 100, 100, 10, 10)
	--love.graphics.rectangle("line", 120+0.5, 140+0.5, 100, 100, 10, 10)
	love.graphics.rectangle("fill", 10, 250, 100, 100, 45, 25)
	love.graphics.setColor(1, 1, 1, 1)
	love.graphics.setFont(c)
	love.graphics.print("Hello from Starstruck!", 20, 20)
	love.graphics.print("dt: "..a, 20, 40)
	local now = love.timer.getTime()
	love.graphics.print("gt: "..now, 20, 60)
	love.graphics.print("1w1", 200, 20, now)
	love.graphics.print("2w2", 200, 20, now, nil, nil, 10, 10)
	love.graphics.print("3w3", 200, 20, now, nil, nil, 20, 20)
	love.graphics.print("4w4", 200, 20, now, 2, 2, 30, 30)
	love.graphics.setFont(d)
	love.graphics.print("STARFALL™!", 20, 80)
	love.graphics.print("STARFALL™!|x", 20, 80+32+20)
end
